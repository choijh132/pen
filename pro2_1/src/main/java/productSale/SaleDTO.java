package productSale;

public class SaleDTO {
	private String productName, id, address, phone, reg;
	private int paymentNum, basketNum, productNum, productPrice, packingPrice, totalPrice;
	private int inkBlack, inkRed, inkBlue, inkGreen;

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getReg() {
		return reg;
	}

	public void setReg(String reg) {
		this.reg = reg;
	}

	public int getPaymentNum() {
		return paymentNum;
	}

	public void setPaymentNum(int paymentNum) {
		this.paymentNum = paymentNum;
	}

	public int getBasketNum() {
		return basketNum;
	}

	public void setBasketNum(int basketNum) {
		this.basketNum = basketNum;
	}

	public int getProductNum() {
		return productNum;
	}

	public void setProductNum(int productNum) {
		this.productNum = productNum;
	}

	public int getProductPrice() {
		return productPrice;
	}

	public void setProductPrice(int productPrice) {
		this.productPrice = productPrice;
	}

	public int getPackingPrice() {
		return packingPrice;
	}

	public void setPackingPrice(int packingPrice) {
		this.packingPrice = packingPrice;
	}

	public int getTotalPrice() {
		return totalPrice;
	}

	public void setTotalPrice(int totalPrice) {
		this.totalPrice = totalPrice;
	}

	public int getInkBlack() {
		return inkBlack;
	}

	public void setInkBlack(int inkBlack) {
		this.inkBlack = inkBlack;
	}

	public int getInkRed() {
		return inkRed;
	}

	public void setInkRed(int inkRed) {
		this.inkRed = inkRed;
	}

	public int getInkBlue() {
		return inkBlue;
	}

	public void setInkBlue(int inkBlue) {
		this.inkBlue = inkBlue;
	}

	public int getInkGreen() {
		return inkGreen;
	}

	public void setInkGreen(int inkGreen) {
		this.inkGreen = inkGreen;
	}

}
