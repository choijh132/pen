package productList;

public class ProductListDTO {
	private int productNum, productPrice, reviewCount, saleCount;
	private int productInkRedStock, productInkBlueStock, productInkGreenStock, productInkBlackStock;
	private String productName, productBrand, productFileName, productLGroup, productSGroup, productOption;

	public int getProductNum() {
		return productNum;
	}

	public void setProductNum(int productNum) {
		this.productNum = productNum;
	}

	public int getProductPrice() {
		return productPrice;
	}

	public void setProductPrice(int productPrice) {
		this.productPrice = productPrice;
	}

	public int getReviewCount() {
		return reviewCount;
	}

	public void setReviewCount(int reviewCount) {
		this.reviewCount = reviewCount;
	}

	public int getSaleCount() {
		return saleCount;
	}

	public void setSaleCount(int saleCount) {
		this.saleCount = saleCount;
	}

	public int getProductInkRedStock() {
		return productInkRedStock;
	}

	public void setProductInkRedStock(int productInkRedStock) {
		this.productInkRedStock = productInkRedStock;
	}

	public int getProductInkBlueStock() {
		return productInkBlueStock;
	}

	public void setProductInkBlueStock(int productInkBlueStock) {
		this.productInkBlueStock = productInkBlueStock;
	}

	public int getProductInkGreenStock() {
		return productInkGreenStock;
	}

	public void setProductInkGreenStock(int productInkGreenStock) {
		this.productInkGreenStock = productInkGreenStock;
	}

	public int getProductInkBlackStock() {
		return productInkBlackStock;
	}

	public void setProductInkBlackStock(int productInkBlackStock) {
		this.productInkBlackStock = productInkBlackStock;
	}

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public String getProductBrand() {
		return productBrand;
	}

	public void setProductBrand(String productBrand) {
		this.productBrand = productBrand;
	}

	public String getProductFileName() {
		return productFileName;
	}

	public void setProductFileName(String productFileName) {
		this.productFileName = productFileName;
	}

	public String getProductLGroup() {
		return productLGroup;
	}

	public void setProductLGroup(String productLGroup) {
		this.productLGroup = productLGroup;
	}

	public String getProductSGroup() {
		return productSGroup;
	}

	public void setProductSGroup(String productSGroup) {
		this.productSGroup = productSGroup;
	}

	public String getProductOption() {
		return productOption;
	}

	public void setProductOption(String productOption) {
		this.productOption = productOption;
	}

}
